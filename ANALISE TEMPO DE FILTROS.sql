SELECT A01.BASE, A01.COD_CRED, A01.NOME_CRED, A01.COD_FIL, A01.DESC_FIL, A01.CONTRATOS, A02.CONTRATOS, A03.CONTRATOS, A04.CONTRATOS,
	A05.CONTRATOS,  A07.CONTRATOS, A08.CONTRATOS, A09.CONTRATOS, A10.CONTRATOS, A11.CONTRATOS, A12.CONTRATOS, A13.CONTRATOS, A14.CONTRATOS,
	A16.CONTRATOS
FROM APLICACAO_0102 A01
	INNER JOIN APLICACAO_0202 A02 WITH (NOLOCK) ON A01.DESC_FIL = A02.DESC_FIL 
	INNER JOIN APLICACAO_0302 A03 WITH (NOLOCK) ON A01.DESC_FIL = A03.DESC_FIL
	INNER JOIN APLICACAO_0402 A04 WITH (NOLOCK) ON A01.DESC_FIL = A04.DESC_FIL
	INNER JOIN APLICACAO_0502 A05 WITH (NOLOCK) ON A01.DESC_FIL = A05.DESC_FIL
	INNER JOIN APLICACAO_0702 A07 WITH (NOLOCK) ON A01.DESC_FIL = A07.DESC_FIL
	INNER JOIN APLICACAO_0802 A08 WITH (NOLOCK) ON A01.DESC_FIL = A08.DESC_FIL
	INNER JOIN APLICACAO_0902 A09 WITH (NOLOCK) ON A01.DESC_FIL = A09.DESC_FIL
	INNER JOIN APLICACAO_1002 A10 WITH (NOLOCK) ON A01.DESC_FIL = A10.DESC_FIL
	INNER JOIN APLICACAO_1102 A11 WITH (NOLOCK) ON A01.DESC_FIL = A11.DESC_FIL
	INNER JOIN APLICACAO_1202 A12 WITH (NOLOCK) ON A01.DESC_FIL = A12.DESC_FIL
	INNER JOIN APLICACAO_1302 A13 WITH (NOLOCK) ON A01.DESC_FIL = A13.DESC_FIL
	INNER JOIN APLICACAO_1402 A14 WITH (NOLOCK) ON A01.DESC_FIL = A14.DESC_FIL
	INNER JOIN APLICACAO_1602 A16 WITH (NOLOCK) ON A01.DESC_FIL = A16.DESC_FIL

		WHERE A01.DESC_FIL NOT LIKE '%RESTANTE%' AND A01.CONTRATOS = 0 AND A02.CONTRATOS = 0 AND A03.CONTRATOS = 0 AND A04.CONTRATOS = 0 AND 
			A05.CONTRATOS = 0 AND A07.CONTRATOS = 0 AND A08.CONTRATOS = 0 AND A09.CONTRATOS = 0 AND A10.CONTRATOS = 0 AND A11.CONTRATOS = 0 AND A12.CONTRATOS = 0
			AND A13.CONTRATOS = 0 AND A14.CONTRATOS = 0 AND A16.CONTRATOS = 0

GROUP BY A01.BASE, A01.COD_CRED, A01.NOME_CRED, A01.COD_FIL, A01.DESC_FIL, A01.CONTRATOS, A02.CONTRATOS, A03.CONTRATOS, A04.CONTRATOS,
	A05.CONTRATOS,  A07.CONTRATOS, A08.CONTRATOS, A09.CONTRATOS, A10.CONTRATOS, A11.CONTRATOS, A12.CONTRATOS, A13.CONTRATOS, A14.CONTRATOS,
	A16.CONTRATOS
ORDER BY A01.DESC_FIL DESC
