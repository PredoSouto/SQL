T.COD_CRED	,
'16' AS COD_OPERACAO	,
TMP.[CNPJ]  AS CPF_CNPJ	,
'1' AS TIPO_TEL	,
'' AS COD_CREDOR,
LEFT (TMP.[telefone], 2) AS DDD,     -------- NOME DA COLUNA DE TELEFONE
