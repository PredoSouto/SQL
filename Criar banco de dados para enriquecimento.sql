INNER JOIN TITULOS T WITH(NOLOCK) ON T.COD_TIT = SP.COD_TIT
INNER JOIN  V_DEVEDORES VD WITH(NOLOCK) ON VD.COD_DEV = T.COD_DEV
INNER JOIN VSQL0.SISTEMAS.[DBO].[277_movel] TMP WITH(NOLOCK) ON TMP.[CNPJ] = VD.CPFCGC_PES collate SQL_Latin1_General_CP1_CI_AS

WHERE T.COD_CRED = 277
